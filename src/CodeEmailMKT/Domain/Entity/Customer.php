<?php

namespace CodeEmailMKT\Domain\Entity;

class Customer
{

    private $id;

    private $name;
	
    private $email;	


    public function getId()
    {
        return $this->id;
    }

    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    public function getName()
    {
        return $this->name;
    }
	
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    public function getEmail()
    {
        return $this->email;
    }	
	
}